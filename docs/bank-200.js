function expandMid(seed){
  const forms = [seed];
  if(!/[ًٌٍ]$/.test(seed)) forms.push(seed + "ً");
  if(!seed.endsWith("ه")) forms.push(seed + "ه");
  return uniq(forms);
}
function expandEnd(seed){
  const forms = [seed];
  if(!/[ًٌٍ]$/.test(seed)) forms.push(seed + "ً");
  if(!/^ال/.test(seed)) forms.push("ال" + seed);
  if(!seed.endsWith("ه")) forms.push(seed + "ه");
  return uniq(forms);
}

function buildList(base, expander, target){
  const out=[]; let i=0;
  while(out.length<target){
    const seed = base[i % base.length];
    const ex = expander(seed);
    for(const w of ex){
      if(out.length<target) out.push(w); else break;
    }
    i++; if(i>3000) break;
  }
  return uniq(out).slice(0, target);
}

const MID_LIST = buildList(BASE_MID, expandMid, 100);
const END_LIST = buildList(BASE_END, expandEnd, 100);

const BANK = [
  ...MID_LIST.map(w => ({ prompt:w, correct:w })),
  ...END_LIST.map(w => ({ prompt:w, correct:w })),
];

